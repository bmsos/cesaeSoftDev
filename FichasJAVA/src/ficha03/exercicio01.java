package ficha03;

public class exercicio01 {

    // Faça um programa que imprima os números no intervalo de 1 a 250 inclusive.
    // (Não necessita de Scanner ou input do utilizador).

    public static void main(String[] args) {



        int n = 1;

        while (n <= 250) {
            System.out.println(n);
            n += 1;
        }

    }
}
